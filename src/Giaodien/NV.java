    private void jButton12ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton12ActionPerformed
        String makh = txtmakh.getText();
        String tenkh = txttenkh.getText();
       
        String sdt = txtsdt.getText();
          String diachi = txtdiachi.getText();
      
        
       
        if (!(txtmakh.getText().equalsIgnoreCase("") || txttenkh.getText().equalsIgnoreCase("") || txtsdt.getText().equalsIgnoreCase("") || txtdiachi.getText().equalsIgnoreCase("") )) {
            if (kiemtrarangbuockhachhang()== true) {
            
                Khachhang kh = new Khachhang(makh, tenkh, sdt, diachi);
                if (dskh.kttrung(kh.getMakh()) == false) {
                    if (dskh.themkhachhang(kh)) {
                        datamodel1.addRow(new Object[]{kh.getMakh(),kh.getTenkh(),kh.getSdt(),kh.getDiachi()});
                        JOptionPane.showMessageDialog(this, "Thêm thành công");
                        txtmakh.setText("");
                        txtmakh.setEnabled(true);
                        txttenkh.setText("");
                        txttenkh.setEnabled(true);
                        txtsdt.setText("");
                        txtsdt.setEnabled(true);
                        txtdiachi.setText("");
                        txtdiachi.setEnabled(true);
                     

                    }
                } else {
                    JOptionPane.showMessageDialog(this, "Mã KH đề không được trùng");
                }

            }
        } else 
            JOptionPane.showMessageDialog(this, "Không được để trống");
        
    }//GEN-LAST:event_jButton12ActionPerformed
    public boolean kiemtrarangbuockhachhang(){
         if (!messloimakh.getText().equalsIgnoreCase("")) {
            JOptionPane.showMessageDialog(this, "Thông tin không hợp lệ");
            return false;
        } else if (!messloitenkh.getText().equalsIgnoreCase("")) {
            JOptionPane.showMessageDialog(this, "Thông tin không hợp lệ");
            return false;
        } else if (!messloidiachi.getText().equalsIgnoreCase("")) {
            JOptionPane.showMessageDialog(this, "Thông tin không hợp lệ");
            return false;
        } else if (!messloisdt.getText().equalsIgnoreCase("")) {
            JOptionPane.showMessageDialog(this, "Thông tin không hợp lệ");
            return false;
        } 
        
        return true;
    }
   public void updatetabledatakhachhang() {

        ArrayList<Khachhang> list = dskh.doctubang();

        for (Khachhang x : list) {
            String[] rowdata = {x.getMakh(),x.getTenkh(),x.getSdt(),x.getDiachi()};
            datamodel1.addRow(rowdata);
        }
        tablequanlykh.setModel(datamodel1);
     
    }
   
   
